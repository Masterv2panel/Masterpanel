prompt_var() {
    local label="$1" default="$2" var_ref="$3" secret="${4:-no}"
    if [[ "$secret" == "yes" ]]; then
        echo -ne "  ${CYAN}➤${R} ${B}${label}${R} ${DIM}[پیش‌فرض: ${default}]${R}: "
        read -rs val; echo ""
    else
        echo -ne "  ${CYAN}➤${R} ${B}${label}${R} ${DIM}[پیش‌فرض: ${default}]${R}: "
        read -r val
    fi
    [[ -z "$val" ]] && val="$default"
    printf -v "$var_ref" '%s' "$val"
}


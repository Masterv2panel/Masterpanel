DETECTED_IP=$(curl -s --max-time 6 https://api.ipify.org 2>/dev/null \
    || curl -s --max-time 6 https://ifconfig.me 2>/dev/null \
    || echo "YOUR-SERVER-IP")

prompt_var "نام کاربری پنل"            "admin"         PANEL_USER
